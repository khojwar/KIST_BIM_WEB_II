<?php

// upper case
echo strtoupper("hello world"); // Output: HELLO WORLD

$string = "Hello World";
echo strtoupper($string); // Output: HELLO WORLD

// lower case
echo strtolower("HELLO WORLD"); // Output: hello world

$string = "HELLO WORLD";
echo strtolower($string); // Output: hello world

// ucfirst() - Converts the first character of a string to uppercase
echo ucfirst("hello world"); // Output: Hello world

$string = "hello world";
echo ucfirst($string); // Output: Hello world

// concatination
echo "Hello" . " " . "World"; // Output: Hello World

?>